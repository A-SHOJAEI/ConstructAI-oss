@router.post("/sync")
async def trigger_full_sync(
    current_user: User = Depends(require_permission("procurement", "create")),
    db: AsyncSession = Depends(get_db),
):
    """Trigger a full Procore data sync for the user's organization.

    Creates a SyncLog entry and syncs all projects and their entities
    (RFIs, documents, change orders, budget, daily logs).
    """
    from app.models.sync_log import SyncLog
    from app.services.integrations.procore_oauth import get_connection_status
    from app.services.integrations.procore_sync import sync_all

    # Check connection exists
    conn_status = await get_connection_status(current_user.org_id, db)
    if not conn_status or not conn_status.get("connected"):
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="No active Procore connection. Connect first via /integrations/procore/connect.",
        )

    # Check for already-running sync
    from sqlalchemy import select as sa_select

    running = await db.execute(
        sa_select(SyncLog).where(
            SyncLog.org_id == current_user.org_id,
            SyncLog.status == "running",
        )
    )
    if running.scalar_one_or_none():
        raise HTTPException(
            status_code=status.HTTP_409_CONFLICT,
            detail="A sync is already in progress for this organization.",
        )

    # Run sync
    sync_log = await sync_all(
        db=db,
        org_id=current_user.org_id,
        triggered_by=current_user.id,
    )

    return {
        "sync_id": str(sync_log.id),
        "status": sync_log.status,
        "entities_synced": sync_log.entities_synced,
        "error_count": len(sync_log.errors) if sync_log.errors else 0,
    }


@router.get("/sync/status")
async def get_sync_status(
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Get the latest sync status for the user's organization."""
    from sqlalchemy import select as sa_select

    from app.models.sync_log import SyncLog

    result = await db.execute(
        sa_select(SyncLog)
        .where(SyncLog.org_id == current_user.org_id)
        .order_by(SyncLog.started_at.desc())
        .limit(1)
    )
    sync_log = result.scalar_one_or_none()

    if sync_log is None:
        return {"has_synced": False, "message": "No sync history found."}

    return {
        "has_synced": True,
        "sync_id": str(sync_log.id),
        "sync_type": sync_log.sync_type,
        "status": sync_log.status,
        "started_at": sync_log.started_at.isoformat() if sync_log.started_at else None,
        "completed_at": sync_log.completed_at.isoformat() if sync_log.completed_at else None,
        "entities_synced": sync_log.entities_synced,
        "error_count": len(sync_log.errors) if sync_log.errors else 0,
        "errors": sync_log.errors[:10] if sync_log.errors else [],
    }


@router.post("/sync/{project_id}")
async def trigger_project_sync(
    project_id: uuid.UUID,
    current_user: User = Depends(require_permission("procurement", "create")),
    db: AsyncSession = Depends(get_db),
):
    """Trigger a Procore data sync for a single project.

    The project must already exist with a procore_id (previously synced).
    """
    await verify_project_access(project_id, current_user, db)

    from sqlalchemy import select as sa_select

    from app.models.procore_connection import ProcoreConnection
    from app.models.project import Project
    from app.services.integrations.procore_sync import sync_project_data

    # Load project record (access already verified above)
    result = await db.execute(
        sa_select(Project).where(
            Project.id == project_id,
            Project.org_id == current_user.org_id,
        )
    )
    project = result.scalar_one_or_none()
    if not project:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Project not found.",
        )

    if not project.procore_id:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="This project is not linked to Procore. Run a full sync first.",
        )

    # Get company_id from connection
    conn_result = await db.execute(
        sa_select(ProcoreConnection).where(ProcoreConnection.organization_id == current_user.org_id)
    )
    conn = conn_result.scalar_one_or_none()
    if not conn or not conn.procore_company_id:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="No Procore connection found.",
        )

    sync_result = await sync_project_data(
        db=db,
        org_id=current_user.org_id,
        project_id=project.id,
        procore_project_id=project.procore_id,
        company_id=int(conn.procore_company_id),
    )

    return {
        "status": "completed" if not sync_result["errors"] else "partial",
        "project_id": project_id,
        "entities_synced": sync_result["entities_synced"],
        "error_count": len(sync_result["errors"]),
    }
