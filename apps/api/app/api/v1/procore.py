@router.get("/connect")
async def procore_connect(
    current_user: User = Depends(require_permission("procurement", "create")),
):
    """Initiate Procore OAuth flow.

    Generates an authorization URL and redirects the user's browser to
    Procore's login page. After the user grants access, Procore redirects
    back to /callback with an authorization code.
    """
    from app.services.integrations.procore_oauth import (
        ProcoreOAuthError,
        generate_auth_url,
    )

    try:
        auth_url = await generate_auth_url(current_user.id, current_user.org_id)
        return RedirectResponse(url=auth_url, status_code=status.HTTP_302_FOUND)
    except ProcoreOAuthError as exc:
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail=str(exc),
        ) from exc


@router.get("/callback")
async def procore_callback(
    code: str = Query(
        ..., min_length=1, max_length=512, description="Authorization code from Procore"
    ),
    state: str = Query(..., min_length=1, max_length=512, description="CSRF state token"),
    db: AsyncSession = Depends(get_db),
):
    """Handle Procore OAuth callback.

    SECURITY (C-05): This endpoint is intentionally not protected by
    ``get_current_user`` because it is invoked via browser redirect from
    Procore's authorization server.  Security relies on:

    1. The ``state`` parameter is a cryptographic token generated by
       ``generate_auth_url()`` and stored server-side with the originating
       ``user_id`` and ``org_id``.  ``exchange_code()`` validates the state
       against the stored value and rejects replays (single-use).
    2. The authorization ``code`` is only valid for a short window and
       can only be exchanged once.
    3. We do not return sensitive tokens in the response — only
       connection status metadata.

    Returns a success message (in production, redirect to the frontend).
    """
    from app.services.integrations.procore_oauth import (
        ProcoreOAuthError,
        exchange_code,
    )

    try:
        conn = await exchange_code(code=code, state=state, db=db)

        # Auto-register webhooks for real-time updates
        webhook_result = None
        if conn.procore_company_id:
            try:
                from app.services.integrations.procore_webhooks import (
                    register_webhooks,
                )

                webhook_result = await register_webhooks(
                    org_id=conn.organization_id,
                    company_id=int(conn.procore_company_id),
                    db=db,
                )
            except Exception as wh_exc:
                logger.warning("Webhook registration failed: %s", wh_exc)

        logger.info(
            "Procore OAuth callback completed for org %s",
            conn.organization_id,
        )

        return {
            "status": "connected",
            "organization_id": str(conn.organization_id),
            "connected_at": conn.connected_at.isoformat() if conn.connected_at else None,
            "message": "Procore connection established successfully.",
            "webhooks": webhook_result,
        }
    except ProcoreOAuthError as exc:
        logger.warning("Procore OAuth callback failed: %s", exc)
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=str(exc),
        ) from exc


@router.get("/status")
async def procore_status(
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Get the current Procore connection status for the user's organization."""
    from app.services.integrations.procore_oauth import get_connection_status

    result = await get_connection_status(current_user.org_id, db)
    if result is None:
        return {"connected": False}
    return result


@router.post("/disconnect")
async def procore_disconnect(
    current_user: User = Depends(require_permission("procurement", "delete")),
    db: AsyncSession = Depends(get_db),
):
    """Disconnect Procore integration for the user's organization.

    Removes stored OAuth tokens from the database.
    """
    from app.services.integrations.procore_oauth import disconnect_procore

    removed = await disconnect_procore(current_user.org_id, db)
    if not removed:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="No Procore connection found for this organization.",
        )
    return {"status": "disconnected", "message": "Procore connection removed."}


